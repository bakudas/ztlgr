    #[test]
    fn test_first_content_line_empty() {
        assert_eq!(first_content_line(""), "");
    }

    #[test]
    fn test_first_content_line_only_headings() {
        assert_eq!(first_content_line("# Title\n## Subtitle"), "");
    }

    #[test]
    fn test_first_content_line_with_frontmatter() {
        let content = "---\ntitle: Foo\n---\n\nContent line";
        assert_eq!(first_content_line(content), "Content line");
    }

    #[test]
    fn test_first_content_line_normal() {
        assert_eq!(
            first_content_line("# Heading\n\nFirst real line"),
            "First real line"
        );
    }

    // =====================================================================
    // IndexGenerator.generate() integration tests
