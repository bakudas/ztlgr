    #[test]
    fn test_write_index_creates_file() {
        let (db, _temp) = create_test_db();

        db.create_note(&make_note("Test", "Content", NoteType::Permanent))
            .expect("Failed");

        // Create the .ztlgr dir (normally done by Vault::initialize)
        let vault_path = _temp.path();
        std::fs::create_dir_all(vault_path.join(".ztlgr")).unwrap();

        let gen = IndexGenerator::new(&db);
        gen.write_index(vault_path).expect("Failed to write index");

        let index_path = vault_path.join(".ztlgr").join("index.md");
        assert!(index_path.exists());

        let content = std::fs::read_to_string(index_path).unwrap();
        assert!(content.contains("# Grimoire Index"));
        assert!(content.contains("[[Test]]"));
    }
